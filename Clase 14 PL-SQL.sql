-- SENTENCIA "CASE"


-- LA SENTENCIA CASE EN PL/SQL SE UTILIZA PARA REALIZAR CONDICIONES COMPLEJAS DE MANERA MÁS EFICIENTE Y ORGANIZADA. 






-- TIPOS DE CASE:


-- CASE SIMPLE: SE UTILIZA CUANDO QUIERES COMPARAR UNA EXPRESIÓN CON DIFERENTES VALORES POSIBLES.


-- CASE DE BÚSQUEDA: ES MÁS FLEXIBLE Y TE PERMITE EVALUAR MÚLTIPLES CONDICIONES.







-- SINTAXIS CASE SIMPLE:

/*


CASE [EXPRESION]

	WHEN [VALOR] THEN

   	 -- BLOQUE DE CÓDIGO SI LA EXPRESIÓN ES IGUAL A VALOR1

	WHEN [VALOR] THEN

  	  -- BLOQUE DE CÓDIGO SI LA EXPRESIÓN ES IGUAL A VALOR2

	ELSE

   	 -- BLOQUE DE CÓDIGO SI NO COINCIDE CON NINGUNO DE LOS VALORES

END CASE;


*/



-- EJEMPLO: 




DECLARE

    V_SALARIO DECIMAL(10, 2) := 3500;

BEGIN

    CASE V_SALARIO

        WHEN 3500 THEN

            DBMS_OUTPUT.PUT_LINE('El salario es 3500');

        WHEN 4000 THEN

            DBMS_OUTPUT.PUT_LINE('El salario es 4000');

        ELSE

            DBMS_OUTPUT.PUT_LINE('Salario desconocido');
            
    END CASE;

END;










-- SINTAXIS CASE DE BÚSQUEDA:


/*



CASE

	WHEN <CONDICIÓN1> THEN

	-- BLOQUE DE CÓDIGO SI LA CONDICIÓN1 ES VERDADERA

	WHEN <CONDICIÓN2> THEN

	-- BLOQUE DE CÓDIGO SI LA CONDICIÓN2 ES VERDADERA

	ELSE

	-- BLOQUE DE CÓDIGO SI NINGUNA DE LAS CONDICIONES ES VERDADERA

END CASE;





*/



-- EJEMPLO:


DECLARE

    NOTA INT := 85;
    
BEGIN

    CASE

        WHEN NOTA >= 90 THEN

            DBMS_OUTPUT.PUT_LINE('Excelente');

        WHEN NOTA >= 70 THEN

            DBMS_OUTPUT.PUT_LINE('Aprobado');

        ELSE

            DBMS_OUTPUT.PUT_LINE('Reprobado');

    END CASE;

END;








-- FORMA DE USAR LOS CASE:


-- EXPRESION CASE: DEVUELVE UN VALOR AL QUE PUEDES ASIGNAR A UNA VARIABLE O USAR EN UNA EXPRESIÓN.


-- SETENCIA CASE: EJECUTA ACCIONES O BLOQUES DE CÓDIGO BASADOS EN UNA O MÁS CONDICIONES.








-- EJEMPLO EXPRESION CASE:


SELECT CASE 

           WHEN SALARIO > 4000 THEN 'Alto'

           WHEN SALARIO BETWEEN 2000 AND 4000 THEN 'Medio'

           ELSE 'Bajo'

       END AS RANGO_SALARIO

FROM EMPLEADOS_EB;









-- EJEMPLO SETENCIA CASE:



DECLARE
    SALARIO DECIMAL(10, 2) := 3500;
BEGIN
    CASE 
        WHEN SALARIO > 3000 THEN
            DBMS_OUTPUT.PUT_LINE('Salario alto');
        WHEN SALARIO BETWEEN 2000 AND 3000 THEN
            DBMS_OUTPUT.PUT_LINE('Salario medio');
        ELSE
            DBMS_OUTPUT.PUT_LINE('Salario bajo');
    END CASE;
END;








 
